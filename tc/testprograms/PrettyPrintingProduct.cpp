class PrettyPrintingProduct {
public:string prettyPrint(int A, int B) {	
		long long cur=1;
		int E=0;
		for(int i=A;i<=B;i++){
			cur*=i;
			while(cur>=1 && cur%10==0){
				E++;
				cur/=10;
			}
		}
		string dev=tos(cur);
		if(dev.sz>10)dev=dev.substr(0,5)+"..."+dev.substr(dev.sz-5);
		return dev+" * 10^"+tos(E);		
				
	}
	//Powered by [Ziklon]
};
