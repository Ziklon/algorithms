class BuyingFlowers {
public:
	int buy(vector <int> roses, vector <int> lilies) {
		int dev=1<<30;
		for(int i=1;i<(1<<roses.sz);i++){
			int a=0,b=0;
			for(int j=0;j<roses.sz;j++)
				if(i&(1<<j)){
					a+=roses[j];
					b+=lilies[j];
				}
			int x=a+b;		
			for(int k=1;k*k<=x+1 && abs(a-b)<=1;k++)
				if(x%k==0 && k*(x/k)==x ){
					dev=min(dev,abs(k-x/k));							
			}							
		}		
		return (dev==1<<30)?-1:dev;
	}
};
