int dp(int n){
  if(n==cmp.sz)return 0;
  if(n>cmp.sz)return 1<<30;  
  if(memo[n]!=-1)return memo[n];
  int dev=1<<30;
  for(int j=cmp.sz-n;j>=1;j--){
    string s=cmp.substr(n,j);
    if(startWith(s)|| endWith(s)){
      dev=min(dev,dp(n+j)+1);
    }
  }
  memo[n]=dev;
  return dev;
}

