class AdditionGame {
public:
	int getMaximumPoints(int A, int B, int C, int N) {
		vector<int>v(3);
		v[0]=A;
		v[1]=B;
		v[2]=C;
		int dev=0,x;
		for(int i=0;i<N;i++){
			sort(rall(v));
			x=v[0];
			if(x>=1)dev+=x;
			v[0]=x-1;	
		}
		return dev;
	}
};
