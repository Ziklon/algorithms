class CatAndRabbit {
public:string getWinner(string tiles) {	
		int ans=0;
		if(tiles.find("#")==-1)return "Rabbit";
		for(int i=0;i<tiles.sz;i++){
			int j=i;
			int cnt=0;
			while(j<tiles.sz && tiles[j]=='.'){
				cnt++;
				j++;			
			}
			i=j;
			
			ans^=cnt;				
		}
		if(ans!=0)return "Cat";
		return "Rabbit";		
	}
};
