class HandsShaking {
public:
	long long countPerfect(int n) {
		ll dp[55][55];
		for(int i=0;i<55;i++)dp[i][0]=1,dp[i][i]=1;
		
		for(int i=1;i<55;i++)dp[0][i]=0;
		
		n/=2;		
		for(int i=1;i<55;i++)
			for(int j=1;j<i;j++)
				dp[i][j]=dp[i-1][j-1]+dp[i-1][j];
		
								
		return dp[2*n][n]-dp[2*n][n+1];
	}
};
