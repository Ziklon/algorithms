class ExerciseMachine {
public:

	int getPercentages(string tm) {
		stringstream ss(tm);
		char p;
		int t=0,h=0,m=0,s=0,nv=0;
		ss >>h>>p>>m>>p>>s;
		t=(h*3600)+(m*60)+s;
		cout << t <<endl;
		for(int i=1;i<100;i++){
			if((t*i)%100==0){
				nv=100/i;
				break;
			}
		}
		nv--;
		if(nv<0)
			nv=0;
		if(nv>99)
			nv=99;			
		return nv;
	}
};


