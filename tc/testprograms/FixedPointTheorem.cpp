class FixedPointTheorem {
public:
	double cycleRange(double R) {
	  double x=0.25;
		for(int i=0;i<=200000;i++)
			x=R*x*(1.0-x);		
		
		double maX=0,miN=100000;
		
		for(int i=0;i<1000;i++){
		  maX=max(maX,x);
		  miN=min(miN,x);
			x=R*x*(1.0-x);
		}
		return abs(maX-miN);
	}
};


