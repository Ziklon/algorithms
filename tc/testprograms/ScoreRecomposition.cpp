		for(int j=0;j<seq.sz;j++)v[j]=j;
		
		do{
			t=0;
			d=0;
			for(int i=0;i<seq.sz;i++){
				if(seq[i]=='C')
					t+=(v[i]+1);
				d=max(d,abs(i-v[i]));
			}
			if(t==score)
				dev=min(dev,d);
		}while(next_permutation(all(v)));
		
