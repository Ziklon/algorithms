int memo[1000001][11];
int top=0;
int dp(int n,int k){
  if(k==top) return n;
  if(memo[n][k]!=-1)return memo[n][k];
  string s=tos(n);
  int dev=0;
  for(int i=0;i<s.sz;i++){
    for(int j=i+1;j<s.sz;j++){    
      swap(s[i],s[j]);
      dev=max(dp(toi(s),k+1),dev);
      swap(s[i],s[j]);       
    }
  }
  memo[n][k]=dev;
  return dev;
 }
