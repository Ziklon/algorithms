int G[55][55];
int v[26];
int cnt;
bool vis[55];
void dfs(string& s, int u){
	vis[u]=1;
	v[s[u]-'a']++;
	cnt++;
	for(int i=0;i<55;i++)if(G[u][i] && !vis[i]){
		dfs(s,i);
	}
}

class NewArenaPassword {
public:int minChange(string oldPassword, int K) {	
		int ans=0;
		int N=oldPassword.size();
		memset(G,0,sizeof G);
		for(int i=0;i<K;i++){
			G[i][N-K+i]=1;
			G[N-K+i][i]=1;
		}
		memset(vis,0,sizeof vis);
		for(int i=0;i<N;i++)if(!vis[i]){
			cnt=0;
			memset(v,0,sizeof v);
			dfs(oldPassword,i);
			int tmp=0;
			for(int j=0;j<26;j++)tmp=max(tmp,v[j]);
			ans+=cnt-tmp;
		}
		return ans;		
	}
};



