double back(int c){
	if(c>=21)return 0;
	double dev=0.0;
	int cont=0;
	for(int i=2;i<12;i++){
		if(card[i]>0){
			for(int j=0;j<card[i];j++){
				card[i]--;
				dev+=(back(c+i));
				card[i]++;
				cont++;
			}		
		}
	}
	dev=dev/cont;
	return 1+ dev;
}

