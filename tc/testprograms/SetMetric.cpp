int go(int p, int q){
	if(p>=T.sz)return 0;
	if(q>=C.sz)return INF;
	int& ref=dp[p][q];
	if(ref!=-1)return ref;
	//case toma dos
	ref=INF;
	ref=min(ref,go(p+1,q+1)+abs(T[p]-C[q]));
	ref=min(ref,go(p,q+1));
	return ref;
}

