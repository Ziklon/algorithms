bool isMega(int n){
	vector<int> v;
	while(n){
		v.pb(n%10);
		n/=10;
	}
	for(int i=0;i+2<v.sz;i++)
		if(v[i+1]-v[i]!=v[i+2]-v[i+1])
			return 0;
	return 1;
}

