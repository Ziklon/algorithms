#define MOD 1234567891
vector<string>v;
int go(int mask, int p){
	int dev=0;
	int col=color(v[p][1]);
	int nu=num(v[p][0]);
	if(__builtin_popcount(mask)==v.sz)return 1;
	if(dp[mask][p]!=-1)return dp[mask][p];
	for(int i=0;i<v.sz;i++){
		if(col==color(v[i][1]) || nu==num(v[i][0])){
			if((mask&(1<<i))==0)
				dev+=(go(mask+(1<<i),i))%MOD;
		}
	}
	dp[mask][p]=dev;
	return dev;
}

