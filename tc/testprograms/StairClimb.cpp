class StairClimb {
public:
	int stridesTaken(vector <int> fl, int st) {
		int nv=0;
		for(int i=0;i<fl.size();i++){
			nv+=(fl[i]+st-1)/st;
		}
		nv+= fl.size()>1 ? 2*(fl.size()-1):0;
		return nv;		
	}
};


