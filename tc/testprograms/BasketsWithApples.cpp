class BasketsWithApples {
public:
	int removeExcess(vector <int> apps) {
	  int dev=0;
	  for(int i=0;i<apps.size();i++){
	    int pe=0;
	    for(int j=0;j<apps.size();j++){
	      if(apps[j]>=apps[i])
	        pe+=apps[i];
	    }
	    dev=max(dev,pe);
	  }
	  return dev;
		
	}
};


