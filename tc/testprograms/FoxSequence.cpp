int progress(vector<int>& v, int a, int b){
	if(b==a)
		return 0;
	int k=v[a+1]-v[a];	
	for(int i=a;i+1<=b;i++){
		if(v[i+1]-v[i]!=k)
			return 1<<30;
	}
	return k;
}
class FoxSequence {
public:
	string isValid(vector <int> seq) {
		int n=seq.sz;		
		for(int i=1;i<seq.sz;i++)
			for(int j=i+1;j<seq.sz;j++)
				for(int k=j;k<seq.sz;k++)
					for(int m=k+1;m<seq.sz;m++){
						int a=progress(seq,0,i);
						int b=progress(seq,i,j);
						int c=progress(seq,j,k);
						int d=progress(seq,k,m);
						int e=progress(seq,m,n-1);
						if(a!=1<<30 && d!=1<<30 && a>0 && b<0 && c==0 && d>0 && e<0)
								return "YES";
					}
		return "NO";	
	}
};
