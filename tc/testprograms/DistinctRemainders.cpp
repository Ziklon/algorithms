typedef long long ll;
#define MOD 1000000007
ll dp[2000][55];
int L;
ll go(int s, int idx){
	if(s==0)return 1;
	if(s<0)return 0;
	ll& ref=dp[s][idx];
	if(ref!=-1)return ref;
	ref=0;
	for(int i=idx;i<L;i++)
		ref+=go(s-i,i+1);
	return ref;
}
class DistinctRemainders {
public:int howMany(long long N, int M) {	
		int ans=0;
		memset(dp,-1,sizeof dp);
		L=M;
		ans=go((int)min(100LL,N),0);
		return ans;		
	}
};
