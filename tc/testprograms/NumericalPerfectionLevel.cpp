class NumericalPerfectionLevel {
public:int getLevel(long long N) {	
		int facts=0;
		for(ll i=2;i*i<=N;i++){
			while(N%i==0){
				facts++;
				N/=i;
			}
		}
		if(N>1)facts++;
		int ans=0;
		cout<<facts<<endl;
		while(facts>=4){
			ans++;
			facts/=4;
		}
		return ans;		
	}
};



