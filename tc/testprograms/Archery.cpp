		vector<double> dis(55);
		double tot=M_PI*(N+1)*(N+1);
		double dev=0;
		dis[0]=0;
		for(int i=1;i<55;i++)
			dis[i]=(M_PI*(i)*(i))-dis[i-1];
		for(int i=0;i<ringPoints.sz;i++){
			dev+=(dis[i+1]/tot)*ringPoints[i];
			cout<<dev<<endl;
		}
		return dev;
				
