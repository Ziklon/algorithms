int MOD= 1000000007;
class TheCowDivTwo {
public:
	int find(int N, int K) {
		long long dp[2][K+2][N];
		for(int i=0;i<2;i++)
			for(int j=0;j<(K+2);j++)
				for(int k=0;k<N;k++)
					dp[i][j][k]=0;

		dp[N&1][K][0]=1;
		for(int i=N-1;i>=0;i--){
			for(int c=0;c<=K;c++)
				for(int s=0;s<N;s++)
					dp[i&1][c][s]=(dp[(i+1)&1][c+1][(s+i)%N]+dp[(i+1)&1][c][s])%MOD;
		}
		return dp[0][0][0];	
	}
};



