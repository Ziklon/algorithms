bool isPal(string s){
	string tmp=s;
	reverse(tmp.begin(),tmp.end());
	return tmp==s;
}

class FauxPalindromes {
public:
	string classifyIt(string word) {
		if(isPal(word))return "PALINDROME";

		string text=word.substr(0,1);

		for(int i=1;i<word.size();i++){
			if(text[text.size()-1]!=word[i]){
				text+=word[i];	
			}
		}
		cout<<text<<endl;
		if(isPal(text))return "FAUX";

		return "NOT EVEN FAUX";

	}
};



