class BifidSortMachine {
public:
	int countMoves(vector <int> a) {
		int dp[55];
		int n=a.size();
		int ans=0;
		for(int i=n-1;i>=0;i--){
			dp[i]=1;
			for(int j=i+1;j<n;j++)
				if(a[i]<a[j])
					dp[i]=max(dp[i],dp[j]+1);
			ans=max(dp[i],ans);
		}
		return n-ans;
	}
};
