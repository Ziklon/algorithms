class DayPlanner {
public:string getEnds(vector <string> tasks) {	
		int fs=1<<30;
		int ls=0;
		string a="";
		string b="";
		for(int i=0;i<tasks.sz;i++){
			int m,h;
			char buff[100];
			sscanf(tasks[i].c_str(),"%d:%d %s",&h,&m,buff);
			if(h*60 + m >ls ){
				ls=h*60+m;
				b=buff;
			}
			if(h*60+ m<fs){
				fs=h*60+m;
				a=buff;
			}
		}
		return a+"-"+b;
				
	}
	//Powered by [Ziklon]
};
