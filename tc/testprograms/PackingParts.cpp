class PackingParts {
public:
	int pack(vector <int> partSizes, vector <int> boxSizes) {
	  bool vis[boxSizes.size()];
	  int dev=0;
	  sort(partSizes.begin(),partSizes.end());
	  sort(boxSizes.begin(),boxSizes.end());
	  int j=0;
	  for(int i=0;i<partSizes.size();i++){
	    for(;j<boxSizes.size();j++){
	      if(partSizes[i]<=boxSizes[j]){
	        dev++;
	        j++;
	        break;
	      }
	    }
	  }
	  return dev;
	}
};


