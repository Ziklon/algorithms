int go(int n){
	if(n==N)return 1;
	if(n>N)return 0;
	int& ref=dp[n];
	if(ref!=-1)return ref;
	ref=0;
	for(int i=n;i<=N;i++)
		ref+=go(n+i);
	return ref;
}
