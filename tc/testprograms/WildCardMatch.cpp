int go(int x, int y){
	if(x==f.sz)return p.sz-y;
	if(y==p.sz)return f.sz-x;
	int& ref=dp[x][y];
	if(ref!=-1)return ref;
	ref=1<<30;
	ref=min(ref,go(x+1,y+1) + (f[x]!=p[y]&& p[y]!='?') );
	ref=min(ref,1+go(x+1,y));
	ref=min(ref,1+go(x,y+1));
	return ref;
}

