public:int minAmount(vector <int> goods) {	
		sort(all(goods));
		int dev=0,t=0;
		for(int i=0;i<goods.sz;i++){
			t+=goods[i];
			dev+=goods[i];
			if(t>=50){
				t=0;
				dev-=10;
			}
		}
		return dev;		
	}
