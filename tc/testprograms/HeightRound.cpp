class HeightRound {
public:
	vector <int> getBestRound(vector <int> h) {
		sort(h.begin(),h.end());
		int N=h.size();
		vector<int> u;
		vector<int> v;
		int d=0;
		for(int i=0;i+2<N;i++)d=max(d,h[i+2]-h[i]);
		int last=h[0];
		u.push_back(last);

		for(int i=1;i+1<N;i++)
			if(h[i+1]-last<=d)u.push_back(h[i]);
			else{
				v.push_back(h[i]);
				last=h[i];
			}
		vector<int> ans;
		for(int i=0;i<u.size();i++)ans.push_back(u[i]);
		ans.push_back(h.back());		
		for(int i=v.size()-1;i>=0;i--)ans.push_back(v[i]);		

		return ans;
	}
};



