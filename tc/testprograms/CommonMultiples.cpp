using namespace std;
typedef long long ll;

ll gcd(ll a, ll b){
	if(b==0)return a;
	return gcd(b,a%b);
}

class CommonMultiples {
public:
	int countCommMult(vector <int> a, int lower, int upper) {
		ll mul=1;
		for(int i=0;i<a.size();i++){
			ll tmp=gcd(mul,a[i]);
			mul=(mul*a[i])/tmp;
			if(mul>upper)return 0;
		}
		ll xa=lower/mul;
		ll xb=upper/mul;
		int ans=xb-xa;
		if(lower%mul==0)xb++;
		ans=xb-xa;
		return ans;
	}
};



