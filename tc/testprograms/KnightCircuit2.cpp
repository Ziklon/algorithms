class KnightCircuit2 {
public:
	int maxSize(int w, int h) {
		if(min(w,h)==1)return 1;
		if(min(w,h)==2) return ((max(w,h)+1)/2);
		if(w==3 && h==3)return 8;
		return w*h;
	}
};



