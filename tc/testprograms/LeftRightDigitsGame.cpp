#define sz size()

class LeftRightDigitsGame {
public:string minNumber(string digits) {	
		int n=digits.sz;
		string ans(n,'9');
		int a=0;
		int b=n-1;
		for(int i=n-1;i>=0;i--){
			char mini='9';
			for(int j=0;j<=i;j++){
				if((a!=0 || digits[j]!='0'))
					mini=min(mini,digits[j]);
			}
			if(digits[i]==mini)ans[a++]=digits[i];
			else ans[b--]=digits[i];		
		}		
		return ans;				
	}
};



