int dp[100050];
int go(int num){
	if(num<10)return 1;
	int& ref=dp[num];
	if(ref!=-1)return ref;
	string name=tos(num);
	ref=0;
	int n=name.sz-1;
	for(int mask=1;mask<(1<<n);mask++){
		int prod=1;
		int last=0;
		for(int j=0;j<n;j++){
			if(mask&(1<<j)){
				prod*=toi(name.substr(last,j-last+1));
				last=j+1;
			}	
		}
		prod*=toi(name.substr(last));
		ref=max(ref,1+go(prod));		
	}	
	return ref;
		
}

