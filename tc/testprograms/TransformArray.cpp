public:int doTransform(vector <int> elements) {	
		sort(all(elements));
		int dev=0;
		for(int i=0;i+1<elements.sz;i++){
			dev=dev+elements[i];
			elements[i+1]-=elements[i];			
		}
		return dev;		
	}
