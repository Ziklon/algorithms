public:vector <string> reportDuplicates(vector <string> cust) {	
		map<string,int> mp;
		map<string,int>::iterator it;
		for(int i=0;i<cust.sz;i++)
			mp[cust[i]]++;
		cust.clear();
		for(it=mp.begin();it!=mp.end();it++){
		  int sec=(*it).second;
		  if(sec>1)
		  	cust.pb((*it).first+" "+tos(sec));
		}
		return cust;		
	}
