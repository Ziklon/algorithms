public:string type(int a, int b, int c) {	
		int dat[3]={a,b,c};
		sort(dat,dat+3);
		a=dat[0];b=dat[1];c=dat[2];
		if(c>=a+b)return "IMPOSSIBLE";
		if(c*c==a*a+b*b)return "RIGHT";
		if(c*c<a*a+b*b)return "ACUTE";
		return "OBTUSE";		
	}
