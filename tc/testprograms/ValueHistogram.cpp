class ValueHistogram {
public:
	vector <string> build(vector <int> values) {
		vector<int> M(10,0);
		for(int i=0;i<values.size();i++)M[values[i]]++;
		int H=0;
		for(int i=0;i<10;i++)H=max(H,M[i]);
				
		vector<string> ans(H+1,string(10,'.'));
		
		for(int i=0;i<10;i++){			
			for(int j=0;j<M[i];j++){
				ans[H -j][i]='X';
			}
		}
		
		
		return ans;
	}
};
