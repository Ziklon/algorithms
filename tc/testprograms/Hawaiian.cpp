public:vector <string> getWords(string sentence) {	
		string hawa="aeiouhklmnpw",s;
		vector<string> dev;
		stringstream ss(sentence);
		while(ss>>s){
			bool ok=1;
			for(int i=0;i<s.sz;i++){
				char c=tolower(s[i]);
				if(hawa.find(c)==string::npos){
					ok=0;
					break;
				}
			}
			if(ok)dev.push_back(s);
		}
		return dev;		
	}
