class TheNumberGameDiv2 {
public:int minimumMoves(int A, int B) {	
		int ans;
		string start=tos(A);
		string goal=tos(B);
		map<string,int> M;
		queue<string> Q;
		Q.push(start);
		M[start]=0;
		while(!Q.empty()){
			string cur=Q.front();Q.pop();

			if(cur==goal){
				return M[cur];
			}

			string tmp=cur;
			reverse(tmp.begin(),tmp.end());
			if(M.count(tmp)==0){
				M[tmp]=M[cur]+1;
				Q.push(tmp);
			}

			if(cur.size()>1){
				tmp=cur.substr(0,cur.size()-1);
				if(M.count(tmp)==0){
					M[tmp]=M[cur]+1;
					Q.push(tmp);
				}
			}			
		}
		return -1;		
	}
};



