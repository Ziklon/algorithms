typedef long long ll;
class MonstersValley2 {
public:
	int minimumPrice(vector <int> dread, vector <int> price) {
		int ans=1<<20;
		int N=price.size();
		for(int mask=1;mask<(1<<N);mask+=2){			
			int cost=price[0];
			ll scare=dread[0];
			bool can=1;
			for(int i=1;i<N;i++){
				if(mask&(1<<i))scare+=dread[i],cost+=price[i];
				else if(scare<dread[i])can=0;				
			}
			if(can)ans=min(ans,cost);																													
		}	
		return ans;
	}
};



