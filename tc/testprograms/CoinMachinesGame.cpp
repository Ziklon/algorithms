struct point
{
  int need;
  int give;
  int n;
};
point game[100];

bool comp(point a,point b)
{
  return a.n < b.n;
}

class CoinMachinesGame
{ 
public:   
    int maxGames(int coins, vector <int> need, vector <int> give)
  {
    int res=0;
    int size=need.size();
    for(int i=0;i<size;i++)
    {
      game[i].need=need[i];
      game[i].give=give[i];
      game[i].n=need[i]-give[i];
    }
    sort(game,game+size,comp);
    for(int i=0;i<size;i++)
    {
      while(coins >= game[i].need)
      {
        coins=coins-game[i].n;
        res++;
      }
    }
    return res;
    
  }

};
