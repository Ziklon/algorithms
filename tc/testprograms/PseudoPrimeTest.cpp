long long fastExp(long long p, long long q, long long m){
	if(q<=0)return 1;
	long long dev=fastExp(p,q/2,m)%m;
	dev=(dev*dev)%m;
	if(q&1)
		dev=(dev*p)%m;
	return dev;
}

