typedef long long ll;

class AmountApproximation {
public:int approximate(int D, int p1, int p2) {	
		if(D%p1==0 || D%p2==0)return D;
		if(p1<p2)swap(p1,p2);
		ll ans=1LL<<50;
		for(ll i=0;i<min(p2,D/p1+2);i++){
			ll val=i*p1;
			if(val<D)val+=(D-val+p2-1)/p2 * p2;
			ans=min(ans,val);					
		}
		return (int)ans;						
	}
};



