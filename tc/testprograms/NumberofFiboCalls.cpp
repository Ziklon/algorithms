int dp[50][3];

int go(int n, int c){
	if(n<0)return 0;
	int& ref=dp[n][c];
	if(ref!=-1)return ref;	
	ref=(n==c);
	if(n==1 || n==0 )return ref;
	ref=go(n-1,c)+go(n-2,c);
	return ref;	
}

