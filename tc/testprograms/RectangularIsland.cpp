double dp[101][101][101];
bool vis[101][101][101];
int dx[]={0,0,1,-1};
int dy[]={1,-1,0,0};
int W,H;
double go(int x, int y, int c){
	if(c==0)return 1.0;
	double& ref=dp[x][y][c];
	if(vis[x][y][c])return ref;
	vis[x][y][c]=1;
	ref=0;
	for(int i=0;i<4;i++){
		int X=x+dx[i];
		int Y=y+dy[i];
		if(min(X,Y)>=0 && X<W && Y<H )
			ref+=(.25*go(X,Y,c-1));
	}	
	return ref;

}

class RectangularIsland {
public:double theProbablity(int width, int height, int x, int y, int steps) {	
		fill(vis,0);
		W=width;
		H=height;
		return go(x,y,steps);				
	}
};
