class FoxAndGCDLCM {
public:long long get(long long G, long long L) {	
		if(L%G!=0)return -1;
		ll unit=L/G;
		ll a,b;
		ll ans=1LL<<60;
		for(ll i=1;i*i<=unit;i++){
			if(unit%i==0){
				a=i;
				b=(unit/a);
				if(__gcd(a,b)==1){

					ans=min(ans,(a+b)*G);
				}
			}
		}
		if(ans==1LL<<60)return -1;
		return ans;

	}

};



