class TypingDistance {
public:int minDistance(string keyboard, string word) {	
		int ans=0;
		int ubi=keyboard.find(word[0]);
		for(int i=1;i<word.sz;i++){
			int tmp=keyboard.find(word[i]);
			ans+=abs(tmp-ubi);
			ubi=tmp;

		}
		return ans;		
	}
};



