typedef long long ll;
ll dp[55][3];
ll go(int idx, int who){
	if(idx==0)return 1LL;
	ll& ref=dp[idx][who];
	if(ref!=-1)return ref;
	ref=0;
	if(who==0)ref+=go(idx-1,2)+go(idx-1,0);
	else ref+=go(idx-1,0)+go(idx-1,1)+go(idx-1,2);
	return ref;	
}

