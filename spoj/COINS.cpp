map<long long,long long>M;

long long go(long long n){
	if(n==0)return 0;
	if(M.count(n)==1)return M[n];
	long long ans=max(n,go(n/2)+go(n/3)+go(n/4));
	M[n]=ans;
	return ans;
}


void solve(){
	long long n,res;	
	while(scanf("%lld",&n)==1){
		res=go(n);
		cout<<res<<endl;
	}
	
}
